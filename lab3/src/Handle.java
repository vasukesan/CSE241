package lab3;

public class Handle<T> {
	int index;
	boolean valid;
	
	public Handle(int index) {
		this.index = index;
		this.valid = true;
	}
	
	public int getIndex(){
		return this.index;
	}
	
	public void set(int index) {
		this.index = index;
	}
	
	public void invalidate(){
		this.valid = false;
	}
	
	public boolean isValid(){
		return valid;
	}
	
	
}
