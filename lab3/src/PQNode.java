package lab3;

public class PQNode<T> {
	Handle h;
	int key;
	T value;
	
	public PQNode(Handle h, int key, T data) {
		this.h = h;
		this.key = key;
		this.value = data;
		
	}
	
	public int getKey(){
		return this.key;
	}
	
	public T getValue() {
		return this.value;
	}
	
	public Handle getHandle(){
		return this.h;
	}
	
	public void setKey(int key) {
		this.key = key;
	}
	
	public String toString(){
		return "("+key+","+value+")";
		
	}
	
	
}
